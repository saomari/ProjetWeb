package model;

public class Candidature {
	public String combobox;
	
	public Candidature() {
		super();
	}

	public Candidature(String combobox) {
		super();
		this.combobox = combobox;
	}
	
	public void setCombobox(String combobox) {
		this.combobox = combobox;
	}

	public String getCombobox() {
		return combobox;
	}

}