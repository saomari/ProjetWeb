package model;

public class Criteres {
	public String combobox;
	
	public Criteres() {
		super();
	}

	public Criteres(String combobox) {
		super();
		this.combobox = combobox;
	}
	
	public void setCombobox(String combobox) {
		this.combobox = combobox;
	}

	public String getCombobox() {
		return combobox;
	}

}